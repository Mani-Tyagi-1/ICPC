    public static void solve()
    {
        Scanner  R = new Scanner(System.in);
        
        int t = R.nextInt();
        long iceCream = R.nextLong();
        int sadChild = 0;

        while ((t--) > 0) {
            char ch = R.next().charAt(0);
            long ice = R.nextLong();

            if (ch == '+') {
                iceCream += ice;
            } else {
                if (ice > iceCream) {
                    sadChild++;
                } else {
                    iceCream -= ice;
                }
            }
        }
        
        System.out.println(iceCream +" "+ sadChild);
    }
    
    static class Reader 
    { 
        BufferedReader br=new BufferedReader(new InputStreamReader(System.in));
        StringTokenizer st=new StringTokenizer("");
        public String next()
        {
            while(!st.hasMoreTokens())
            {
                try
                {
                    st=new StringTokenizer(br.readLine());
                }
                catch(Exception e)
                {
                    e.printStackTrace();
                }
            }
            return st.nextToken();
        }
        public int nextInt()
        {
            return Integer.parseInt(next());
        }
        public long nextLong()
        {
            return Long.parseLong(next());
        }
        public double nextDouble()
        {
            return Double.parseDouble(next());
        }
        public String nextLine()
        {
            try
            {
                return br.readLine();
            }
            catch(Exception e)
            {
                e.printStackTrace();
            }
            return null;
        }
        public boolean hasNext()
        {
            String next=null;
            try
            {
                next=br.readLine();
            }
            catch(Exception e)
            {
            }
            if(next==null)
            {
                return false;
            }
            st=new StringTokenizer(next);
            return true;
        }
    } 
